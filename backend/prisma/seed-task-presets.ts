async function main() {
  console.log('ðŸŒ± Starting task presets seed...');

  let created = 0;
  let skipped = 0;

  for (const preset of taskPresets) {
    const existing = await prisma.taskPreset.findFirst({
      where: {
        title: preset.title,
        roomType: preset.roomType,
      },
    });

    if (existing) {
      skipped++;
      continue;
    }

    await prisma.taskPreset.create({
      data: {
        ...preset,
        isSystemPreset: true,
      },
    });
    created++;
  }

  console.log(`âœ… Seed completed!`);
  console.log(`ðŸ“Š Created: ${created} task presets`);
  console.log(`â­ï¸  Skipped: ${skipped} existing presets`);
  console.log(`ðŸ“ˆ Total in database: ${created + skipped}`);
}

main()
  .catch((e) => {
    console.error('âŒ Error seeding task presets:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
